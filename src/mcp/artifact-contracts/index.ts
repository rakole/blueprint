export type ArtifactTemplateContext = {
  phaseLabel?: string;
  phasePrefix?: string;
  phaseName?: string;
  phaseDir?: string;
  planId?: string;
  domain?: string;
  summaryFile?: string;
  summaryPath?: string;
  reportName?: string;
  milestone?: string;
  currentPhase?: string;
  activeCommand?: string;
};

export type ArtifactContractDefinition = {
  id: ArtifactContractId;
  scope: ArtifactContractScope;
  ownerTool: string;
  pathOwner: string;
  canonicalName: string;
  canonicalFilePattern: string;
  freehandPolicy: ArtifactContractFreehandPolicy;
  requiredHeadings: string[];
  lockedMarkers: string[];
  placeholderSignals: string[];
  notes: string[];
  renderScaffoldTemplate: (context?: ArtifactTemplateContext) => string;
  renderAuthoringTemplate: (context?: ArtifactTemplateContext) => string;
};

export type ArtifactContractReadResult = Omit<
  ArtifactContractDefinition,
  "renderScaffoldTemplate" | "renderAuthoringTemplate"
> & {
  scaffoldTemplate: string;
  authoringTemplate: string;
};

function phaseLabel(context?: ArtifactTemplateContext): string {
  return context?.phaseLabel?.trim() || "Phase XX: <Phase Name>";
}

function phasePrefix(context?: ArtifactTemplateContext): string {
  return context?.phasePrefix?.trim() || "XX";
}

function planId(context?: ArtifactTemplateContext): string {
  return context?.planId?.trim() || "YY";
}

function reportName(context: ArtifactTemplateContext | undefined, fallback: string): string {
  return context?.reportName?.trim() || fallback;
}

function summaryFile(context?: ArtifactTemplateContext): string {
  return context?.summaryFile?.trim() || `${phasePrefix(context)}-${planId(context)}-SUMMARY.md`;
}

function summaryPath(context?: ArtifactTemplateContext): string {
  return (
    context?.summaryPath?.trim() ||
    `.blueprint/phases/<phase-dir>/${summaryFile(context)}`
  );
}

function phaseDir(context?: ArtifactTemplateContext): string {
  return context?.phaseDir?.trim() || "<phase-dir>";
}

function milestone(context?: ArtifactTemplateContext): string {
  return context?.milestone?.trim() || "<milestone-version>";
}

function currentPhase(context?: ArtifactTemplateContext): string {
  return context?.currentPhase?.trim() || "none";
}

function activeCommand(context?: ArtifactTemplateContext): string {
  return context?.activeCommand?.trim() || "/blu-pause-work";
}

function domain(context?: ArtifactTemplateContext): string {
  return context?.domain?.trim() || "<research domain>";
}

function withScaffoldFooter(content: string): string {
  return `${content}\n\n---\n*Generated by \`blueprint_artifact_scaffold\`*\n`;
}

