export function resolveReportContractId(
  name: string
): ArtifactContractId | null {
  const normalized = name.trim().toLowerCase();

  if (normalized === "pause-work-latest" || normalized === "pause-work") {
    return "report.pause-work";
  }

  if (normalized.startsWith("milestone-audit-")) {
    return "report.milestone-audit";
  }

  if (normalized.startsWith("milestone-complete-")) {
    return "report.milestone-complete";
  }

  if (normalized.startsWith("milestone-summary-")) {
    return "report.milestone-summary";
  }

  if (normalized === "debug-latest") {
    return "report.debug";
  }

  if (normalized === "quick-run-latest") {
    return "report.quick-run";
  }

  if (normalized === "docs-update-latest") {
    return "report.docs-update";
  }

  if (normalized === "pr-branch-latest") {
    return "report.pr-branch";
  }

  if (normalized === "ship-latest") {
    return "report.ship";
  }

  if (normalized === "undo-latest") {
    return "report.undo";
  }

  if (normalized === "cleanup-latest") {
    return "report.cleanup";
  }

  if (normalized.startsWith("add-tests-")) {
    return "report.add-tests";
  }

  if (normalized.startsWith("audit-fix-")) {
    return "report.audit-fix";
  }

  return null;
}
