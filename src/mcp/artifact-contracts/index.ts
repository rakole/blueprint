export const artifactContractIds = Object.keys(
  ARTIFACT_CONTRACTS
) as ArtifactContractId[];

export function getArtifactContract(
  contractId: ArtifactContractId
): ArtifactContractDefinition {
  return ARTIFACT_CONTRACTS[contractId];
}

export function readArtifactContract(
  contractId: ArtifactContractId,
  context?: ArtifactTemplateContext
): ArtifactContractReadResult {
  const contract = getArtifactContract(contractId);

  return {
    id: contract.id,
    scope: contract.scope,
    ownerTool: contract.ownerTool,
    pathOwner: contract.pathOwner,
    canonicalName: contract.canonicalName,
    canonicalFilePattern: contract.canonicalFilePattern,
    freehandPolicy: contract.freehandPolicy,
    requiredHeadings: [...contract.requiredHeadings],
    lockedMarkers: [...contract.lockedMarkers],
    placeholderSignals: [...contract.placeholderSignals],
    notes: [...contract.notes],
    scaffoldTemplate: contract.renderScaffoldTemplate(context),
    authoringTemplate: contract.renderAuthoringTemplate(context)
  };
}

export function listArtifactContracts(): ArtifactContractReadResult[] {
  return artifactContractIds.map((contractId) => readArtifactContract(contractId));
}

export function renderArtifactAuthoringTemplate(
  contractId: ArtifactContractId,
  context?: ArtifactTemplateContext
): string {
  return getArtifactContract(contractId).renderAuthoringTemplate(context);
}

export function renderArtifactScaffoldTemplate(
  contractId: ArtifactContractId,
  context?: ArtifactTemplateContext
): string {
  return getArtifactContract(contractId).renderScaffoldTemplate(context);
}

export function resolvePhaseArtifactContractId(
  artifact: "context" | "discussion-log" | "research" | "ui-spec"
): ArtifactContractId {
  switch (artifact) {
    case "context":
      return "phase.context";
    case "discussion-log":
      return "phase.discussion-log";
    case "research":
      return "phase.research";
    case "ui-spec":
      return "phase.ui-spec";
  }
}

export function resolveValidationArtifactContractId(
  artifact: "verification" | "uat"
): ArtifactContractId {
  return artifact === "verification" ? "phase.verification" : "phase.uat";
}

export function resolveReviewArtifactContractId(
  artifact: "code-review" | "peer-review" | "review-fix" | "security" | "ui-review"
): ArtifactContractId {
  switch (artifact) {
    case "code-review":
      return "review.code-review";
    case "peer-review":
      return "review.peer-review";
    case "review-fix":
      return "review.review-fix";
    case "security":
      return "review.security";
    case "ui-review":
      return "review.ui-review";
  }
}

export function resolveReportContractId(
  name: string
): ArtifactContractId | null {
  const normalized = name.trim().toLowerCase();

  if (normalized === "pause-work-latest" || normalized === "pause-work") {
    return "report.pause-work";
  }

  if (normalized.startsWith("milestone-audit-")) {
    return "report.milestone-audit";
  }

  if (normalized.startsWith("milestone-complete-")) {
    return "report.milestone-complete";
  }

  if (normalized.startsWith("milestone-summary-")) {
    return "report.milestone-summary";
  }

  if (normalized === "debug-latest") {
    return "report.debug";
  }

  if (normalized === "quick-run-latest") {
    return "report.quick-run";
  }

  if (normalized === "docs-update-latest") {
    return "report.docs-update";
  }

  if (normalized === "pr-branch-latest") {
    return "report.pr-branch";
  }

  if (normalized === "ship-latest") {
    return "report.ship";
  }

  if (normalized === "undo-latest") {
    return "report.undo";
  }

  if (normalized === "cleanup-latest") {
    return "report.cleanup";
  }

  if (normalized.startsWith("add-tests-")) {
    return "report.add-tests";
  }

  if (normalized.startsWith("audit-fix-")) {
    return "report.audit-fix";
  }

  return null;
}
